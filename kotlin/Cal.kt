class Cal ( var num1:Int, var num2:Int){

    fun add(){
        val added = num1 + num2
        println("Added: $added")
    }

    fun sub(){
        val sub = num1 - num2
        println("sub: $sub")
    }

    fun div(){
        val div = num1 / num2
        println("div: $div")
    }

    fun multi(){
        val multi = num1 * num2
        println("multi: $multi")
    }

}